use anyhow::Result;
use sqlx::PgPool;

// ── Parent table DDL ──────────────────────────────────────────────────────────

/// Create the three K8s partitioned parent tables if they don't already exist.
/// Safe to call repeatedly — all DDL uses IF NOT EXISTS.
pub async fn ensure_parent_tables(pool: &PgPool) -> Result<()> {
    // core."_MatterK8s" — partitioned by PostedOn (immutable creation time)
    sqlx::query(
        r#"CREATE TABLE IF NOT EXISTS core."_MatterK8s" (
            "Id"       BIGINT       NOT NULL,
            "Pid"      BIGINT,
            "Type"     INT          NOT NULL,
            "StateId"  INT          NOT NULL,
            "PostedOn" TIMESTAMPTZ  NOT NULL DEFAULT NOW(),
            "StatedOn" TIMESTAMPTZ  NOT NULL DEFAULT NOW(),
            PRIMARY KEY ("Id", "PostedOn")
        ) PARTITION BY RANGE ("PostedOn")"#,
    )
    .execute(pool)
    .await?;

    // Snowflake IDs are globally unique by construction; a regular index suffices.
    // PG partitioned tables cannot have UNIQUE indexes that exclude the partition key.
    sqlx::query(
        r#"CREATE INDEX IF NOT EXISTS "IX__MatterK8s_Id"
           ON core."_MatterK8s" ("Id")"#,
    )
    .execute(pool)
    .await?;

    sqlx::query(
        r#"CREATE INDEX IF NOT EXISTS "IX__MatterK8s_StateId"
           ON core."_MatterK8s" ("StateId")"#,
    )
    .execute(pool)
    .await?;

    sqlx::query(
        r#"CREATE INDEX IF NOT EXISTS "IX__MatterK8s_Type"
           ON core."_MatterK8s" ("Type")"#,
    )
    .execute(pool)
    .await?;

    // trd."_TradeRebateK8s" — partitioned by CreatedOn (immutable insert time)
    sqlx::query(
        r#"CREATE TABLE IF NOT EXISTS trd."_TradeRebateK8s" (
            "Id"             BIGINT         NOT NULL,
            "AccountId"      BIGINT,
            "TradeServiceId" INT            NOT NULL,
            "Ticket"         BIGINT         NOT NULL,
            "AccountNumber"  BIGINT         NOT NULL,
            "CurrencyId"     INT            NOT NULL DEFAULT -1,
            "Volume"         INT            NOT NULL DEFAULT 0,
            "Status"         INT            NOT NULL DEFAULT 0,
            "RuleType"       INT            NOT NULL DEFAULT 199,
            "CreatedOn"      TIMESTAMPTZ    NOT NULL DEFAULT NOW(),
            "UpdatedOn"      TIMESTAMPTZ    NOT NULL DEFAULT NOW(),
            "ClosedOn"       TIMESTAMPTZ    NOT NULL,
            "OpenedOn"       TIMESTAMPTZ    NOT NULL,
            "TimeStamp"      BIGINT         NOT NULL DEFAULT 0,
            "Action"         INT            NOT NULL DEFAULT 0,
            "DealId"         BIGINT         NOT NULL DEFAULT 0,
            "Symbol"         VARCHAR(32)    NOT NULL DEFAULT '',
            "ReferPath"      VARCHAR(512)   NOT NULL DEFAULT '',
            "Commission"     NUMERIC(18,8)  NOT NULL DEFAULT 0,
            "Swaps"          NUMERIC(18,8)  NOT NULL DEFAULT 0,
            "OpenPrice"      NUMERIC(18,8)  NOT NULL DEFAULT 0,
            "ClosePrice"     NUMERIC(18,8)  NOT NULL DEFAULT 0,
            "Profit"         NUMERIC(18,8)  NOT NULL DEFAULT 0,
            "Reason"         INT            NOT NULL DEFAULT 0,
            PRIMARY KEY ("Id", "CreatedOn")
        ) PARTITION BY RANGE ("CreatedOn")"#,
    )
    .execute(pool)
    .await?;

    // Snowflake IDs are globally unique by construction; a regular index suffices.
    sqlx::query(
        r#"CREATE INDEX IF NOT EXISTS "IX__TradeRebateK8s_Id"
           ON trd."_TradeRebateK8s" ("Id")"#,
    )
    .execute(pool)
    .await?;

    // Per-partition dedup: partition key must be included in unique index
    sqlx::query(
        r#"CREATE UNIQUE INDEX IF NOT EXISTS "UX__TradeRebateK8s_Ticket_ServiceId_CreatedOn"
           ON trd."_TradeRebateK8s" ("Ticket", "TradeServiceId", "CreatedOn")"#,
    )
    .execute(pool)
    .await?;

    sqlx::query(
        r#"CREATE INDEX IF NOT EXISTS "IX__TradeRebateK8s_AccountNumber"
           ON trd."_TradeRebateK8s" ("AccountNumber")"#,
    )
    .execute(pool)
    .await?;

    // acct."_WalletTransactionK8s" — partitioned by CreatedOn (immutable insert time)
    sqlx::query(
        r#"CREATE TABLE IF NOT EXISTS acct."_WalletTransactionK8s" (
            "Id"          BIGINT       GENERATED BY DEFAULT AS IDENTITY,
            "WalletId"    BIGINT       NOT NULL,
            "InvoiceId"   BIGINT,
            "MatterId"    BIGINT       NOT NULL,
            "PrevBalance" BIGINT       NOT NULL DEFAULT 0,
            "Amount"      BIGINT       NOT NULL DEFAULT 0,
            "CreatedOn"   TIMESTAMPTZ  NOT NULL DEFAULT NOW(),
            "UpdatedOn"   TIMESTAMPTZ  NOT NULL DEFAULT NOW(),
            PRIMARY KEY ("Id", "CreatedOn")
        ) PARTITION BY RANGE ("CreatedOn")"#,
    )
    .execute(pool)
    .await?;

    // GENERATED IDENTITY IDs are globally unique; a regular index suffices.
    sqlx::query(
        r#"CREATE INDEX IF NOT EXISTS "IX__WalletTransactionK8s_Id"
           ON acct."_WalletTransactionK8s" ("Id")"#,
    )
    .execute(pool)
    .await?;

    sqlx::query(
        r#"CREATE INDEX IF NOT EXISTS "IX__WalletTransactionK8s_MatterId"
           ON acct."_WalletTransactionK8s" ("MatterId")"#,
    )
    .execute(pool)
    .await?;

    sqlx::query(
        r#"CREATE INDEX IF NOT EXISTS "IX__WalletTransactionK8s_WalletId"
           ON acct."_WalletTransactionK8s" ("WalletId")"#,
    )
    .execute(pool)
    .await?;

    Ok(())
}

// ── Year sub-partition management ─────────────────────────────────────────────

/// Ensure the year sub-partition exists for the given K8s parent table.
/// E.g. ensure_year_partition(pool, "core", "_MatterK8s", 2026) creates
/// core."_MatterK8s_2026" FOR VALUES FROM ('2026-01-01') TO ('2027-01-01').
pub async fn ensure_year_partition(
    pool: &PgPool,
    schema: &str,
    parent: &str,
    year: i32,
) -> Result<()> {
    let partition = format!(r#"{schema}."{}_{year}""#, parent);
    let parent_full = format!(r#"{schema}."{parent}""#);
    let from = format!("{}-01-01", year);
    let to = format!("{}-01-01", year + 1);

    sqlx::query(&format!(
        r#"CREATE TABLE IF NOT EXISTS {partition}
           PARTITION OF {parent_full}
           FOR VALUES FROM ('{from}') TO ('{to}')"#
    ))
    .execute(pool)
    .await?;

    Ok(())
}

// ── _Rebate_{year} (kept as application-level year tables) ────────────────────

/// Ensure trd."_Rebate_{year}" exists.
/// _Rebate has no suitable time column for native partitioning, so it remains
/// an application-level year table alongside the K8s partitioned tables.
pub async fn ensure_rebate_year_table(pool: &PgPool, year: i32) -> Result<()> {
    let r = format!(r#"trd."_Rebate_{}""#, year);
    let ix_account = format!(r#"IX__Rebate_{}_AccountId"#, year);
    let ix_party = format!(r#"IX__Rebate_{}_PartyId"#, year);
    let ix_trade_rebate = format!(r#"IX__Rebate_{}_TradeRebateId"#, year);

    sqlx::query(&format!(
        r#"CREATE TABLE IF NOT EXISTS {r} (
            "Id"            BIGINT        PRIMARY KEY,
            "PartyId"       BIGINT        NOT NULL,
            "AccountId"     BIGINT        NOT NULL,
            "FundType"      INT           NOT NULL DEFAULT 0,
            "CurrencyId"    INT           NOT NULL DEFAULT -1,
            "Amount"        NUMERIC(20,8) NOT NULL DEFAULT 0,
            "TradeRebateId" BIGINT,
            "HoldUntilOn"   TIMESTAMPTZ,
            "Information"   TEXT          NOT NULL DEFAULT ''
        )"#
    ))
    .execute(pool)
    .await?;

    sqlx::query(&format!(
        r#"CREATE INDEX IF NOT EXISTS "{ix_account}" ON {r} ("AccountId")"#
    ))
    .execute(pool)
    .await?;

    sqlx::query(&format!(
        r#"CREATE INDEX IF NOT EXISTS "{ix_party}" ON {r} ("PartyId")"#
    ))
    .execute(pool)
    .await?;

    sqlx::query(&format!(
        r#"CREATE INDEX IF NOT EXISTS "{ix_trade_rebate}" ON {r} ("TradeRebateId")"#
    ))
    .execute(pool)
    .await?;

    Ok(())
}
